namespace Kruty1918.Moyva.WorldCreation.API
{
    /// <summary>
    /// Тип/шаблон карти, що впливає на пресети генераторних налаштувань
    /// (щільність води, гір, лісів тощо).
    /// </summary>
    public enum MapTypePreset
    {
        /// <summary>Рівномірний розподіл усіх біомів. Добре підходить для більшості партій.</summary>
        Balanced    = 0,

        /// <summary>Великі суходільні масиви з невеликою кількістю внутрішніх морів.</summary>
        Continental = 1,

        /// <summary>Переважно вода; невеликі острови з обмеженими ресурсами.</summary>
        Island      = 2,

        /// <summary>Карта насичена горами та скелями; низька щільність лісів.</summary>
        Mountain    = 3,

        /// <summary>Широкі рівнини, мінімум гір; висока щільність сіл і POI.</summary>
        Plains      = 4
    }
}
